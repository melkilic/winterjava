public class EighthMarch {
	/*
	String day= "8MARCH";
	         use charAt method and loop then print all letters on the console
	          */
	
	public static void main(String[] args) {

		String day = "8MARCH";
		for(int i=1; i<=day.length()-1; i++) {
			System.out.println(day.charAt(i));
		}
	}
    } 
		
	

